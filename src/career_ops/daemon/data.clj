   "- `submit.clj` — Signed application package builder\n"
   "- `verify.clj` — Inbound signature verification"))

(def m2m-spec
  (str
   "# M2M Job Application Protocol v1.0.0\n\n"
   "**Machine-to-Machine** job application pipeline. No browsers, no CAPTCHAs, no manual portals.\n\n"
   "### Sub-Protocols\n\n"
   "| Sub-protocol | Description |\n"
   "|---|---|\n"
   "| **Discovery** | Locate employer endpoints via DNS TXT (`_m2m-apply.<domain>`) or directory |\n"
   "| **Fetch** | Retrieve machine-readable job postings (JSON-LD extending schema.org) |\n"
   "| **Verify** | Ed25519 mutual identity verification |\n"
   "| **Submit** | Signed application package delivery + signed acknowledgment |\n\n"
   "### Data Flow\n"
   "1. DNS TXT lookup on `_m2m-apply.employer.example`\n"
   "2. Receive endpoint URL + public key\n"
   "3. GET job posting as JSON-LD from employer\n"
   "4. Run MAS evaluation pipeline\n"
   "5. Tailor resume via Gemini + compile PDF via Typst\n"
   "6. Build signed ApplicationPackage (Ed25519)\n"
   "7. POST multipart form with JSON-LD payload + PDF attachment\n"
   "8. Receive signed acknowledgment with application ID\n\n"
   "### Cryptography\n"
   "- Ed25519 keypairs (Java `java.security`)\n"
   "- Every application is signed; every attachment has its own digest + signature\n"
   "- DNSSEC-ready, DID-compatible (`did:key:`)\n"
   "- Future-proofed for encrypted submissions\n\n"
   "### Threat Model\n"
   "| Threat | Mitigation |\n"
   "|---|---|\n"
   "| Impersonation | DNS-based key binding + TLS |\n"
   "| Replay attack | Timestamp + nonce in package |\n"
   "| Tampered resume | Attachment digest + separate signature |\n"
   "| Man-in-the-middle | TLS + signed payloads for end-to-end integrity |\n\n"
   "### No-CAPTCHA Guarantee\n"
   "- Endpoints opt-in by publishing `m2m:acceptsSignedApplications: true`\n"
   "- Signed identity replaces cookie/session/auth-wall\n"
   "- Rate limiting by public key fingerprint, not IP"))

