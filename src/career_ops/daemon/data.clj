(def modules
  (str
   "# Modules\n\n"
   "## 1. Data Vault (profiler.clj)\n"
   "Single-shot extraction of `master_profile` + `star_stories` from unstructured raw text "
   "(LinkedIn dump, CV text). Uses Gemini with temperature 0.2 for consistency. "
   "Outputs strict JSON saved to `data/master-profile.edn` and `data/star-stories.edn`.\n\n"
   "## 2. Multi-Agent Evaluator (evaluator.clj)\n"
   "3-stage sequential MAS pipeline:\n"
   "- **Agent 1 (Legitimacy):** Parses JD, checks Fair Consideration Framework (FCF) compliance, "
   "identifies red flags. Outputs structured summary block.\n"
   "- **Agent 2 (Fit Analysis):** Brutal comparison of Master Profile vs JD, exact gaps/strengths, "
   "GO/NO-GO recommendation.\n"
   "- **Agent 3 (Cheat Sheet):** Pre-interview cheat sheet: business model, tech stack, "
   "cold outreach strategy with titles + templates.\n\n"
   "## 3. Interview Prep (interview.clj)\n"
   "Cross-references STAR stories library against JD. Predicts 5 most likely interview questions "
   "with STAR mappings, delivery advice, and Reflections column.\n\n"
   "## 4. PDF Resume Generator (pdf.clj)\n"
   "Sends CV + JD to Gemini with ATS-tailoring prompt. Uses structured JSON output (responseMimeType). "
   "Temperature 0.2. Compiles via Typst into a targeted PDF resume.\n\n"
   "## 5. Pipeline Tracker (tracker.clj)\n"
   "Markdown-based application tracker at `data/applications.md`. "
   "Table format with ID, Date, Company, Role, Score, Status, PDF, Report, Notes. "
   "Supports add, list, and mark-PDF-ready operations.\n\n"
   "## 6. M2M Protocol (m2m/*.clj)\n"
   "Decentralized, cryptographic job application pipeline. 7 files:\n"
   "- `core.clj` — CLI entry point and routing\n"
   "- `crypto.clj` — Ed25519 key generation, signing, verification\n"
   "- `schema.clj` — JSON-LD schema validation and generation\n"
   "- `registry.clj` — DNS + directory discovery client\n"
   "- `directory.clj` — Babashka directory server (optional)\n"
